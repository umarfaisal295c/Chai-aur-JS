// >  => less then 
// <  => greater then
// >=  => less then 0r equal then
// <=  =>  greater then 0r equal then
// ==  =>  equal then
// ===  => datatype are equal
//  This is called Comparision Operator in JS.

// Example:-
//
    console.log(2>1);
    console.log(2>=1);
    console.log(2<=1);
//
// 
    console.log("2">1);
    console.log("02"==1);
